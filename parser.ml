  let rec listToFun l e =
    match l with
    | [] -> e
    | h::t -> Fun(h,listToFun t e)

  let rec tuplelen l =
    match l with
    | TupleNode(a,b) -> 1+
        (
          match b with
            | TupleNode(_,_) -> tuplelen b
            | _               -> 1
        )
    | _             -> 0

  let rec make_app e l =
    match l with
    | a :: [] -> App(e,a)
    | a :: r -> make_app (App (e,a)) r
    | []    -> failwith "Error : empty list"

 
